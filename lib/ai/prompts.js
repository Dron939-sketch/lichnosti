export const SYSTEM_PROMPT = `Ты — редактор биографического портала «Личности».
Пиши фактологично, нейтрально, на русском языке, без домыслов.
Если факт не подтверждён — не выдумывай, укажи "нет достоверных данных".
Используй HTML-разметку только из тегов: p, h2, h3, ul, ol, li, strong, em, blockquote, a.`;

export const PROMPTS = {
  full_bio: ({ name, category, context }) => ({
    system: SYSTEM_PROMPT,
    user: `Напиши полную биографическую статью о человеке "${name}"${
      category ? ` (категория: ${category})` : ''
    }.
${context ? `Дополнительный контекст: ${context}\n` : ''}
Структура статьи (используй подзаголовки h2):
- Краткое введение (1 абзац, кто это и чем известен).
- Ранние годы, детство, происхождение.
- Образование.
- Карьера / профессиональный путь (разбей на этапы с h3).
- Личная жизнь (если есть достоверная публичная информация).
- Интересные факты (5–7 пунктов, ul).
- Награды, признание.
- Настоящее время / наследие.
Объём: 800–1500 слов. Ответ — только HTML тела статьи, без <html>, <body>, без код-блоков.`
  }),

  seo_meta: ({ name, bio }) => ({
    system: SYSTEM_PROMPT,
    user: `На основе биографии ниже составь SEO-метаданные для страницы о "${name}".
Верни строго JSON без пояснений:
{
  "title": "не более 60 символов, включает имя и ключевое слово 'биография'",
  "description": "140–160 символов, естественный язык, с CTA 'Читать подробнее'",
  "keywords": "8–12 фраз через запятую, без дублей",
  "h1": "основной заголовок страницы, до 80 символов"
}

Биография:
${bio.slice(0, 4000)}`
  }),

  short_bio: ({ name, bio }) => ({
    system: SYSTEM_PROMPT,
    user: `Сократи биографию ниже до 2–3 предложений (до 280 символов) для карточки "${name}". Только текст, без HTML.

Биография:
${bio.slice(0, 3000)}`
  }),

  similar_persons: ({ name, profession, category }) => ({
    system: SYSTEM_PROMPT,
    user: `Назови 6 реально существующих публичных персон, наиболее похожих на "${name}"${
      profession ? ` (${profession})` : ''
    }${category ? ` из области "${category}"` : ''}.
Верни строго JSON-массив объектов:
[{"name":"ФИО","reason":"почему похож, 1 фраза"}]`
  }),

  has_new_info: ({ name, currentBio, lastUpdate }) => ({
    system: SYSTEM_PROMPT,
    user: `Биография "${name}" последний раз обновлялась ${lastUpdate}.
Проанализируй её (ниже) и реши, появилась ли за это время значимая новая публичная информация (новые роли, награды, события, смерть, резонанс).
Верни строго JSON:
{"has_new": true|false, "new_facts": ["факт 1", "факт 2"]}

Текущая биография:
${currentBio.slice(0, 6000)}`
  }),

  fact_check: ({ name, bio }) => ({
    system: SYSTEM_PROMPT,
    user: `Проверь биографию "${name}" на фактические ошибки, противоречия, выдуманные данные.
Верни JSON:
{"ok": true|false, "issues": ["описание ошибки", ...]}

Биография:
${bio.slice(0, 6000)}`
  })
};

export function buildPrompt(key, vars) {
  const fn = PROMPTS[key];
  if (!fn) throw new Error(`Unknown prompt key: ${key}`);
  return fn(vars || {});
}
