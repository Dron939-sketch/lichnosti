function pickIcon(url) {
  return NETWORKS.find((n) => n.match.test(url));
}

export default function SocialIcons({ links = [] }) {
  const items = (Array.isArray(links) ? links : [])
    .map((l) => (typeof l === 'string' ? { url: l } : l))
    .filter((l) => l && l.url);

  if (!items.length) return null;

  return (
    <li className="social_link">
      {items.map((l, i) => {
        const icon = pickIcon(l.url);
        if (!icon) return null;
        return (
          <a key={i} href={l.url} target="_blank" rel="noopener noreferrer" aria-label={icon.key}>
            {icon.svg}
          </a>
        );
      })}
    </li>
  );
}
